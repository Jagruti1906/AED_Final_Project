    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String[] columnNames = {"Admission Id","Name","Phone Number","Email","School Name", "Date","Status"};
        String[][] rows = new String[AdmissionDirectory.getInstance().getAdmissionDir().size()][7];
        int j=0;
        for(int i=0;i<AdmissionDirectory.getInstance().getAdmissionDir().size();i++) {
             if(AdmissionDirectory.getInstance().getAdmissionDir().get(i).getStatus().equals("Pending")){
                int adId = AdmissionDirectory.getInstance().getAdmissionDir().get(i).getAdmissionId();
                rows[j][0] = Integer.toString(adId);
                rows[j][1] = AdmissionDirectory.getInstance().getAdmissionDir().get(i).getName();
                rows[j][2] = AdmissionDirectory.getInstance().getAdmissionDir().get(i).getPhoneNumber();
                rows[j][3] = AdmissionDirectory.getInstance().getAdmissionDir().get(i).getEmail();
                SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
                String s = formatter.format(AdmissionDirectory.getInstance().getAdmissionDir().get(i).getDate());
                rows[j][5] = s;
                rows[j][4] = AdmissionDirectory.getInstance().getAdmissionDir().get(i).getSchoolName();
                rows[j][6] = AdmissionDirectory.getInstance().getAdmissionDir().get(i).getStatus();
                j++;
            }
        }
        DefaultTableModel model = new DefaultTableModel (rows, columnNames);
        jTable1.setModel(model);
    }//GEN-LAST:event_jButton1ActionPerformed

